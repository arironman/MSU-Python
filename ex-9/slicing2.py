# Get first character of a string
a = "Hello, World!"
print(a[1])
# Get the characters from position 2 to 5
b = "Hello, World!"
print(b[2:5])
#Reverse a string
a="Krishna"
print(a[::-1])
# Length of String
a = "Hello, World!"
print(len(a))
# In Lower case
a = "Hello, World!"
print(a.lower())
a = "Hello, World!"
print(a.upper())
# Upper Case
a = "Hello, World!"
print(a.upper())
