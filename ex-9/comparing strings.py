#Comparision Operator
str1="Ironman"
str2="spidey"
#We can compare it in if condition also
#but here i am going to print the boolean values
#   Code                            Output
print(str1 == str2)             #False
print(str1!= str2)              #True
print(str1 >= str2)             #False
print(str1 <= str2)             #True
print(str1 > str2)              #False
print(str1 < str2)              #True
